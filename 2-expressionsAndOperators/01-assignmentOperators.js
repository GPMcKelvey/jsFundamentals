//assignment operator
let r = 5; //the single equal sign is our assignment operator

//addition assignment operator
r += 1; //r = r + 1

console.log(r); //6

//subtraction assignment operator
r -=1; //r = r - 1 (js files read from top to bottom so the most recent version of r is used)
console.log(r); //5

//multiplication assignment operator
r *= 1; //r = r * 1
console.log(r); //5

//division assignment operator
r /= 2; //r = r / 2
console.log(r);

//remainder (modulus) assignment operator
r %= 2; //r = r % 2
console.log(r);

//exponential assignment operator
r **= 1; //r = r ** 1
console.log(r);